    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        if (Validations.isTextFieldEmpty(txtCRN.getText()) && Validations.isTextFieldEmpty(txtName.getText())) {
            JOptionPane.showMessageDialog(null, "CRN Number and/or Course name cannot be blank!");
        } else {
            int crn, totalSeats;
            try {
                crn = Integer.parseInt(txtCRN.getText());
            } catch (NumberFormatException num) {
                JOptionPane.showMessageDialog(null, "CRN Number has to be an integer value.");
                return;
            }

            for (Courses c : program.getCourses().getCourseList()) {
                if (c.getCrnNumber() == Integer.parseInt(txtCRN.getText())) {
                    JOptionPane.showMessageDialog(null, "CRN number already exists.");
                    return;
                }
            }

            if (Integer.parseInt(txtTotalSeats.getText()) > 1000) {
                JOptionPane.showMessageDialog(null, "Maximum total seats per course has to be less than or equal to 1000");
                return;
            }

            String coursename = txtName.getText().trim();
            try {
                totalSeats = Integer.parseInt(txtTotalSeats.getText());
            } catch (NumberFormatException num) {
                JOptionPane.showMessageDialog(null, "Total seats has to be an integer value.");
                return;
            }

            if (totalSeats < 0) {
                JOptionPane.showMessageDialog(null, "Total seats cannot be negative.");
                return;
            }

            if (Validations.isTextLong(coursename)) {
                JOptionPane.showMessageDialog(null, "Course name cannot exceed 20 characters");
                return;
            }

            String status = "Initiated";
            Courses cc = program.getCourses().createCourse(crn, coursename, status, totalSeats);
            UserAccount account = (UserAccount) cmbProfessorName.getSelectedItem();
            //program.setProfessor(account);
            cc.setProfessor(account);
            cc.setProgram(program);
            program.getProgramDirector().createFeeds(userAccount.getEmployee().getName() + " added new course " + cc.getCourseName() + " to program " + program.getName());
            new File("C:\\Assignment_submission\\" + coursename).mkdir();

            JOptionPane.showMessageDialog(null, "Course Requested");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

