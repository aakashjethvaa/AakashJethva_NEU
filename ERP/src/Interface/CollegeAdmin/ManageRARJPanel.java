    public void populateCombo() {
        cmbStudentsName.removeAllItems();
        for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
            if (ua.getRole() instanceof RARole) {
                cmbStudentsName.addItem(ua);
            }
        }
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblTARequest.getModel();

        model.setRowCount(0);
       
        for(UserAccount ua : org.getUserAccountDirectory().getUserAccountList()){
            if(ua.getRole() instanceof ProfessorRole){
                ProfessorRole role = (ProfessorRole)ua.getRole();
                for(RARequest rr : role.getRArequests()){
                    Object[] row = new Object[2];
                    row[0] = rr;
                    
                    row[1] = rr.getStatus();

                    model.addRow(row);
                }
            }
        }
        
        
        
       
    }

