    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:

        int selected = tblTARequest.getSelectedRow();
        if (selected >= 0) {
            UserAccount ua = (UserAccount) cmbStudentsName.getSelectedItem();
            RARequest tar = (RARequest) tblTARequest.getValueAt(selected, 0);
            UserAccount prof = tar.getProfessor();
            ProfessorRole role = (ProfessorRole)prof.getRole();
            Boolean flag = role.AssignRA(ua);
            if (flag) {
                JOptionPane.showMessageDialog(null, "RA Account asssigned");

                role.removeRequest(tar);
                populateTable();
            } else {
                JOptionPane.showMessageDialog(null, "RA Account already assigned to the course");
            }
        }

    }//GEN-LAST:event_btnApproveActionPerformed

